package com.poc.patient.visualization;

public class VegaMark {
	private String type;
	private VegaMarkFrom from;
	private VegaMarkEncode encode;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public VegaMarkFrom getFrom() {
		return from;
	}
	public void setFrom(VegaMarkFrom from) {
		this.from = from;
	}
	public VegaMarkEncode getEncode() {
		return encode;
	}
	public void setEncode(VegaMarkEncode encode) {
		this.encode = encode;
	}
	
	
}
